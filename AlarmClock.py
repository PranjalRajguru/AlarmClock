    "#     Alarm.after(1000, update_time)\n",
    "\n",
    "# Tkinter Window\n",
    "Alarm = Tk()\n",
    "Alarm.geometry('600x450')\n",
    "Alarm.title(\"Alarm Clock\")\n",
    "\n",
    "Label(Alarm, text=\"My Alarm Clock\", font=(\"Calibri\", 20, \"bold\"), fg='green').pack(pady=10)\n",
    "Label(Alarm, text=\"Set time\", font=(\"Calibri\", 16, \"bold\"), fg='blue').pack()\n",
    "\n",
    "frame = Frame(Alarm)\n",
    "frame.pack()\n",
    "\n",
    "# Hours (12-hour format)\n",
    "hour = StringVar(Alarm)\n",
    "hours = tuple(f\"{i:02}\" for i in range(1, 13))\n",
    "hour.set(hours[0])\n",
    "hr = OptionMenu(frame, hour, *hours)\n",
    "hr.pack(side=LEFT)\n",
    "\n",
    "# Minutes\n",
    "minute = StringVar(Alarm)\n",
    "minutes = tuple(f\"{i:02}\" for i in range(60))\n",
    "minute.set(minutes[0])\n",
    "mins = OptionMenu(frame, minute, *minutes)\n",
    "mins.pack(side=LEFT)\n",
