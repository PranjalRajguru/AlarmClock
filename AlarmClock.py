    "from tkinter import * #GUI design\n",
    "import datetime as dt\n",
    "import time\n",
    "import winsound as ws\n",
    "from threading import Thread\n",
    "\n",
    "# Global control variables\n",
    "stop_alarm = False\n",
    "snooze_time = None\n",
    "\n",
    "def start_alarm_thread():\n",
    "    t1 = Thread(target=alarm)\n",
    "    t1.start()\n",
    "\n",
    "def alarm():\n",
    "    global stop_alarm, snooze_time\n",
    "    stop_alarm = False\n",
    "    snooze_time = None\n",
    "\n",
    "    while True:\n",
    "        if stop_alarm:   # if stop button pressed\n",
    "            break\n",
    "\n",
    "        set_time = f\"{hour.get()}:{minute.get()}:{second.get()} {ampm.get()}\"\n",
    "        time.sleep(1)\n",
    "\n",
    "        # Current time in 12-hour format with AM/PM\n",
    "        curr_time = dt.datetime.now().strftime('%I:%M:%S %p')\n",
    "\n",
    "        if snooze_time and curr_time == snooze_time:  # snooze triggered\n",
    "            ws.PlaySound(\"D:\\\\alarmsong.wav\", ws.SND_ASYNC)#to play sound on alarm\n",
    "            snooze_time = None\n",
    "\n",
    "        elif curr_time == set_time:   # normal alarm trigger\n",
    "            ws.PlaySound(\"D:\\\\alarmsong.wav\", ws.SND_ASYNC)\n",
    "            set_time = None   # alarm stops checking once it rings\n",
    "\n",
    "\n",
    "def stop_alarm_sound():\n",
    "    global stop_alarm\n",
    "    stop_alarm = True\n",
    "    ws.PlaySound(None, ws.SND_PURGE)   # stop any sound immediately\n",
    "\n",
    "def snooze_alarm():\n",
    "    global snooze_time\n",
    "    # Add 5 minutes to current time\n",
    "    snooze_dt = dt.datetime.now() + dt.timedelta(minutes=1)\n",
    "    snooze_time = snooze_dt.strftime('%I:%M:%S %p')\n",
    "    ws.PlaySound(None, ws.SND_PURGE)   # stop current ringing\n",
    "    print(\"Snoozed until:\", snooze_time)\n",
    "\n",
    "# def update_time():\n",
